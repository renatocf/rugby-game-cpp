std::ostream& operator<<(std::ostream& out, const dimension_t& dimension) {
  out << dimension.height << "," << dimension.width;

  return out;
}

/*----------------------------------------------------------------------------*/

std::istream& operator>>(std::istream& in, dimension_t& dimension) {
  char comma;

  in >> dimension.height;
  in >> comma;
  in >> dimension.width;

  return in;
}

